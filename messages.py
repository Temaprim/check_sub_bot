def get_channel_list_string(path: str) -> str:
    db = database.get_csv_db(path)

    channel_list_string = ''
    for channel in db:
        link = channel['invite_link']
        text = channel['name']
        channel_list_string += f'<a href="{link}">{text}</a>\n'

    return channel_list_string


